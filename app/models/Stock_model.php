   private function prepare($rows = [], $options = []) {
      foreach ($rows as $r) {
         $r->colorstatus = $r->state_color == "fontgreen" ? "green" : "red";
         $r->icon = $r->state_color == "fontgreen" ? "fas  fa-caret-up" : "fas fa-caret-down";
         $r->change_percent_formatted = number_format(floatval($r->change_percent), 2);
      }
   
      return $rows;
   }

   private function getCache($type) {
      $response = new \stdClass();
      $cache = $this->session->userdata($type);
      $now = date('Y-m-d H:i:s');
      if (isset($cache->lastcall)) {
         $expired = strtotime("+1 minutes", strtotime($cache->lastcall));
         $expired = date('Y-m-d H:i:s', $expired);
         
         if ($now > $expired) {
            $response = $this->apisdk->call_get($type);
            $response->lastcall = $now;
         } else {
            $response = $cache;
         }
      } else {
         $response = $this->apisdk->call_get($type);
         $response->lastcall = $now;
      }

      if (isset($response->s)) {
         $this->session->set_userdata($type, $response);
      }
      return $response;
   }
